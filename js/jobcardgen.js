
$(document).ready(function() {

// ======================================================
// ================= card number input ==================
// ======================================================

    var cardvolume = 70;

// ======================================================
// ============= card generation equation ===============
// ======================================================

    var el = $(".cardgen");

    for(var i=1; i<cardvolume; i++){
        el.clone().removeClass("cardgen").addClass('job'+i).insertBefore("#anotherjob");
    }

// ======================================================
// =========== add job number to first card =============
// ======================================================

    $(".cardgen").addClass('job0');

  });